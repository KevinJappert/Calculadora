class Calculadora {
    sumar(num1, num2) {
        return num1 + num2
    }
}

class Calculadora {
    restar(num1, num2) {
        return num1 - num2
    }
}

class Calculadora {
    dividir(num1, num2) {
        return num1 / num2
    }
}

class Calculadora {
    multiplicar(num1, num2) {
        return num1 * num2
    }
}